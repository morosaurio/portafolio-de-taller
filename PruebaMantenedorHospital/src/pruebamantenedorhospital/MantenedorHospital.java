    private void verDatos(){
        String Mat[][] = new String[luis.size()][5];
        Paciente aux;
        for (int i = 0; i < luis.size(); i++) {
            aux = luis.get(i);
            Mat[i][0] = aux.getNombre();
            Mat[i][i] = aux.getEdad();
            Mat[i][2] = aux.getRut(); 
            Mat[i][3] = aux.getTelefono()+"";
            Mat[i][4] = aux.getSexo();
            Mat[i][5] = "asd";
        }
         jTabla1.setModel(new javax.swing.table.DefaultTableModel(
            Mat,
            new String [] {
                "Nombre", "Edad", "Rut", "Telefono",  "Sexo"
            }
        ));
         cargartabla();
    
    }//GEN-LAST:event_jButtonAgregarActionPerformed

