    public void cargartabla(){
        TableModel modelo = new TableModel() {
            @Override
            public int getRowCount() {
               return luis.size();
            }

            @Override
            public int getColumnCount() {
              return 5;
            }

            @Override
            public String getColumnName(int columnIndex) {
                String titulo = null;
                
                switch(columnIndex){
                    case 0:
                        titulo = "nombre";
                        break;
                    case 1:
                        titulo = "edad";
                        break;
                    case 2: 
                        titulo = "rut";
                        break;
                    case 3:
                        titulo = "telefono";
                        break;
                    case 4:
                        titulo = "sexo";
                        break;
            }
                return titulo;
            }
            
            @Override
            public Class<?> getColumnClass(int columnIndex) {
                 return String.class;
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return columnIndex != 0;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                Paciente a = luis.get(rowIndex);
            
             String valor = null;
                switch(columnIndex){
                     case 0:
                        valor = a.getNombre();
                        break;
                     case 1:
                         valor = a.getEdad();
                         break;
                     case 2:
                         valor = a.getRut();
                         break;
                     case 3:
                         valor = String.valueOf(a.getTelefono());
                         break;
                     case 4:
                         valor = a.getSexo();
                         break;
                }
                return valor;
                         
            }        

            @Override
            public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
                Paciente a = luis.get(rowIndex);
                
                switch(columnIndex){
                    case 0:
                        a.setNombre(aValue.toString());
                        break;
                    case 1:
                        a.setEdad(aValue.toString());
                        break;
                    case 2:
                        a.setRut(aValue.toString());
                        break;
                    case 3: 
                     a.setTelefono(Integer.parseInt(aValue.toString()));
                        break;
                    case 4:
                        a.setSexo(aValue.toString());
                        break;
                }
            }

            @Override
            public void addTableModelListener(TableModelListener l) {
            }

            @Override
            public void removeTableModelListener(TableModelListener l) {
            }
        
         }; 
        
       jTabla1.setModel(modelo);
    }

